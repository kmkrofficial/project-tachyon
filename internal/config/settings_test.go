package config

import (
	"os"
	"path/filepath"
	"testing"

	"project-tachyon/internal/storage"
)

// newTestConfig creates a ConfigManager backed by a temp SQLite DB.
func newTestConfig(t *testing.T) *ConfigManager {
	t.Helper()
	tmpDir := t.TempDir()
	dbPath := filepath.Join(tmpDir, "test.db")

	// Open storage directly with temp DB path
	s, err := storage.NewStorageWithPath(dbPath)
	if err != nil {
		t.Fatalf("failed to create test storage: %v", err)
	}
	t.Cleanup(func() { s.Close() })

	return NewConfigManager(s)
}

func TestConfigManager_AIPortDefault(t *testing.T) {
	cfg := newTestConfig(t)
	if cfg.GetAIPort() != 4444 {
		t.Fatalf("expected default port 4444, got %d", cfg.GetAIPort())
	}
}

func TestConfigManager_SetGetAIPort(t *testing.T) {
	cfg := newTestConfig(t)
	if err := cfg.SetAIPort(9090); err != nil {
		t.Fatal(err)
	}
	if cfg.GetAIPort() != 9090 {
		t.Fatalf("expected port 9090, got %d", cfg.GetAIPort())
	}
}

