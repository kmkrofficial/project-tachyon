func TestConfigManager_EnableAI(t *testing.T) {
	cfg := newTestConfig(t)
	// Default is false
	if cfg.GetEnableAI() {
		t.Fatal("expected AI disabled by default")
	}
	if err := cfg.SetEnableAI(true); err != nil {
		t.Fatal(err)
	}
	if !cfg.GetEnableAI() {
		t.Fatal("expected AI enabled after set")
	}
}

func TestConfigManager_IntegrityCheckDefault(t *testing.T) {
	cfg := newTestConfig(t)
	if !cfg.GetEnableIntegrityCheck() {
		t.Fatal("expected integrity check enabled by default")
	}
}

func TestConfigManager_TokenAutoGenerated(t *testing.T) {
	cfg := newTestConfig(t)
	token := cfg.GetAIToken()
	if len(token) < 16 {
		t.Fatalf("expected auto-generated token, got %q", token)
	}
	// Should be stable
	if cfg.GetAIToken() != token {
		t.Fatal("token should be stable across calls")
	}
}

func TestConfigManager_UserAgentEmpty(t *testing.T) {
	cfg := newTestConfig(t)
	ua := cfg.GetUserAgent()
	if ua != "" {
		t.Fatalf("expected empty user agent by default, got %q", ua)
	}
}

